import AUIKit

enum Colors {
    
    static var white: UIColor { return .white }
    
    static var red: UIColor { return UIColor(red: 0.9176, green: 0.2392, blue: 0.9686) }
    
    static var blue: UIColor { return UIColor(red: 0, green: 0.1176, blue: 0.9607) }
    
    static var gray: UIColor { return UIColor(red: 0.8313, green: 0.8313, blue: 0.8313) }
    
    static var lightGray: UIColor { return UIColor(red: 0.9098, green: 0.9098, blue: 0.9098) }
    
}
